        async onLoad(options) {

		/*下拉框*/
			let shipinTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'shipin_types',
            }
			let shipinTypes = await this.$api.page(`dictionary`, shipinTypesParams);
			this.shipinTypesOptions = shipinTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`shipin`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.shipinId){
                this.ruleForm.shipinId = options.shipinId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            shipinTypesChange(e) {
                this.shipinTypesIndex = e.target.value
                this.ruleForm.shipinValue = this.shipinTypesOptions[this.shipinTypesIndex].indexName
                this.ruleForm.shipinTypes = this.shipinTypesOptions[this.shipinTypesIndex].codeIndex
            },


            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
